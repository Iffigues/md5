#include "libft.h"

int			ft_atoi(const char *str)
{
	int nbr;
	int s;

	s = 1;
	nbr = 0;
	while (((*str >= 9) && (*str <= 13)) || (*str == 32))
		str++;
	if ((*str == '+') || (*str == '-'))
		s = (',' - *str++);
	while ((*str >= '0') && (*str <= '9'))
		nbr = ((nbr * 10)  + (*str++ - '0'));
	return (nbr * s);
}
